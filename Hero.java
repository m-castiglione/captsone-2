public class Hero {
    public String name;
    public int health;
    public int strength;
    public int defense;
    public int magic;

    public Hero(String name, int health, int strength, int defense, int magic) {
        this.name = name;
        this.health = health;
        this.strength = strength;
        this.defense = defense;
        this.magic = magic;
    }
}
